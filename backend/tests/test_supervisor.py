@patch("app.agents.ConceptGraphAgent.build_graph")
def test_supervisor_graceful_llm_error(mock_build):
    """Verify Supervisor converts LLMError to a structured error status."""
    mock_build.side_effect = LLMError("rate_limit", "Rate limit exceeded")
    
    agent = SupervisorAgent()
    res = agent.route_and_execute("concept_spot", {"doc_id": "doc123", "document_text": "text"})
    
    assert res["status"] == "error"
    assert res["error_category"] == "rate_limit"
    assert "all providers failed" in res["message"]
