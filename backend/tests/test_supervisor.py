"""
Direct unit tests for SupervisorAgent.route_and_execute().
"""

import pytest
from unittest.mock import patch, MagicMock, AsyncMock
from app.supervisor import SupervisorAgent
from app.llm_client import LLMError


def test_supervisor_unknown_action():
    """Verify that unknown actions return a graceful error status."""
    agent = SupervisorAgent()
    res = agent.route_and_execute("invalid_action_name", {})
    assert res["status"] == "error"
    assert "Unknown action" in res["message"]


@patch("app.agents.ConceptGraphAgent.build_graph")
@patch("app.database.save_concepts")
@patch("app.database.save_concept_edges")
def test_supervisor_concept_spot(mock_save_edges, mock_save_concepts, mock_build):
    """Verify concept_spot action delegates to ConceptGraphAgent."""
    mock_build.return_value = ([{"label": "TestNode"}], [{"source": "A", "target": "B"}])
    
    agent = SupervisorAgent()
    res = agent.route_and_execute("concept_spot", {"doc_id": "doc123", "document_text": "sample text"})
    
    assert res["status"] == "success"
    assert res["nodes"] == [{"label": "TestNode"}]
    assert res["edges"] == [{"source": "A", "target": "B"}]
    mock_build.assert_called_once_with("doc123", "sample text", llm_client=agent.llm_client)
    mock_save_concepts.assert_called_once()
    mock_save_edges.assert_called_once()


